/**
* *_memset - function that fills memory with a constant byte
* @n: bytes of memory area pointed to by s
* @s: with the constant byte
* @b: memory area
* Return: A pointer to the memory area at s
*/
char *_memset(char *s, char b, unsigned int n)
{
	unsigned int i = 0;

	while (i < n)
	{
		s[i] = b;
		i++;
	}
	return (s);
}

/**
 * _bfree - Function frees a pointer
 * @ptr: address of the pointer to free
 * Return: Return 1 if freed,else 0 if otherwise
 */
int _bfree(void **ptr)
{
	if (ptr && *ptr)
	{
		free(*ptr);
		*ptr = NULL;
		return (1);
	}
	return (0);
}

/**
 * _ffree - frees a string of strings
 * @pp: string of strings
 */
void _ffree(char **pp)
{
	char **a = pp;

	if (!pp)
		return;
	while (*pp)
		free(*pp++);
	free(a);
}

